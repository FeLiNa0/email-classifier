hash_function pick_hash(int d)
{
    switch (d) {
        case 128:
            return &hash128;
        case 256:
            return &hash256;
        case 512:
            return &hash512;
        case 1024:
            return &hash1024;
        case 2048:
            return &hash2048;
        case 4096:
            return &hash4096;
        case 8192:
            return &hash8192;
        default:
            printf("UNKNOWN PEARSON HASH SIZE REQUESTED: %d\n", d);
            return NULL;
    }
}
